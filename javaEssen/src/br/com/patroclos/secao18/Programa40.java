package br.com.patroclos.secao18;

//Realizando cálculos com Math

/*
 * 
 * A biblioteca Math possui vários métodos e constantes estáticas
 * para que possamos utilizar nos nossos programas. 
 * 
 * 
 * */
public class Programa40 {
	
	
	public static void main(String[] args) {
		
		System.out.println(Math.E); //Constante
	
		System.out.println(Math.PI); //Constante
		
		System.out.println(Math.sin(45)); //seno
		
		System.out.println(Math.cos(45)); //coseno
		
		System.out.println(Math.tan(45)); //Tangente
		
		System.out.println(Math.round(Math.PI)); //Arredondando o valor de PI(3.4455555) para (3)

		System.out.println(Math.ceil(Math.PI)); //Arredonda para cima..PI(3.55777755) para (4)
		
		System.out.println(Math.floor(Math.PI)); // Arredonda pra baixo retorna DOuble
		
		System.out.println(Math.pow(8, 2));// 8² resultado um Double
		
		System.out.println(Math.round(Math.pow(8, 2))); // Convertendo resultado para Inteiro
		
		
		System.out.println(Math.sqrt(9)); // Raiz quadrada de 9
		
		
	}
	
	
	
}
